# make a list with 3 elements
my_list = [1, 2, 3]

# add an element to the end of the list
my_list.append(4)

# remove an element from the list
my_list.remove(2)

# reverse the list
my_list.reverse()

# sort the list
my_list.sort()

# add an element at the start of the list
my_list.insert(0, 0)

# print the index of the last element
print(len(my_list))
