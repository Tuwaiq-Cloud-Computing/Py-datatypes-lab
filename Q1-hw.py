# Initializing a list with 3 elements
my_list = [10, 20, 30]

# Adding an element to the end of the list
my_list.append(40)

# Removing an element from the list
my_list.pop(0)

# Reversing the order of the list
my_list.reverse()

# Sorting the list in ascending order
my_list.sort()

# Adding an element at the start of the list
my_list.insert(0, 5)

# Printing the index of the last element in the list
print("The index of the last element is:", len(my_list)-1)